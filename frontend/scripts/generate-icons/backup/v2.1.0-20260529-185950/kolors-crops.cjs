const ALL_KOLORS_CROPS = [...BASIC_CROPS, ...ECONOMIC_CROPS];

function getCropCategory(id) {
  const crop = ALL_KOLORS_CROPS.find(c => c.id === id);
  if (!crop) return '未知';
  switch (crop.category) {
    case 'leaf_veg': return CROP_CATEGORIES.leaf_veg;
    case 'root': return CROP_CATEGORIES.root;
    case 'fruit': return CROP_CATEGORIES.fruit;
    case 'vine': return CROP_CATEGORIES.vine;
    case 'tree': return CROP_CATEGORIES.tree;
    case 'shrub': return CROP_CATEGORIES.shrub;
    default: return '未知';
  }
}

function getStageLabel(stage) {
  const labels = { 1: '幼苗期', 2: '生长期', 3: '开花期', 4: '结果期', 5: '成熟期' };
  return labels[stage] || '未知';
}

function getAllKolorsTasks(options = {}) {
  const { includeStages = true, includeProducts = true } = options;
  const tasks = [];

  ALL_KOLORS_CROPS.forEach(crop => {
    if (includeStages) {
      for (let stage = 1; stage <= 5; stage++) {
        const stageKey = `stage${stage}`;
        tasks.push({
          type: 'growth_stage',
          id: crop.id,
          stage: stage,
          filename: `${crop.id}_stage_${stage}.png`,
          outputDir: 'crops/stages',
          data: {
            ...crop,
            stageVisual: crop[stageKey],
            stageNum: stage,
            stageLabel: getStageLabel(stage),
            category: getCropCategory(crop.id)
          }
        });
      }
    }
    
    if (includeProducts && crop.product) {
      tasks.push({
        type: 'product',
        id: crop.id,
        filename: `crop_${crop.id}.png`,
        outputDir: 'icons/crops',
        data: {
          ...crop,
          productVisual: crop.product,
          category: getCropCategory(crop.id)
        }
      });
    }
  });

  return tasks;
}

module.exports = {
  CROP_CATEGORIES,
  BASIC_CROPS,
  ECONOMIC_CROPS,
  ALL_KOLORS_CROPS,
  getCropCategory,
  getStageLabel,
  getAllKolorsTasks
};
